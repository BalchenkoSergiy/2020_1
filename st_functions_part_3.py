def f2(l):
    def get_mult(x):
        return int(x) * 2
    return [get_mult(i) for i in l]


print('5 -->:', f2(l))
# [2, 4, 6]



print('**************************************************************************************************************')


def f3(l):
    def get_mult(x):
        if isinstance(x, int):
            return x * 2
    return [get_mult(i) for i in l if get_mult(i)]


print('6 -->:', f3(l))
# [2, 6]

print('**************************************************************************************************************')


def f4(l):
    def get_mult(x):
        return x * 2
    return list(map(get_mult, l))


print('7 -->:', f4(l))
# [2, '22', 6]
